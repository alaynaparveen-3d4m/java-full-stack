class DataTypes{
    public static void main(String[] args) {

        int a = 10;
        char ch = 'a';
        String st = "Hello";
        boolean flag = true;
        float b = (float)10.10;
        double d = 5.123;

        System.out.println(a);
        System.out.println(ch);
        System.out.println(st);
        System.out.println(flag);
        System.out.println(b);
        System.out.println(d);
    }
}